# Escreva um programa que converta uma temperatura digitada em ºC para ºF

# Declaração da variável que pede a temperatura em ºC
celsius = float(input('Digite a temperatura em ºC: '))

# Declaração da variável que converte a temperatura em ºF
fahrenheit = celsius * 1.8 + 32

# Resultado utilizando um print para a temperatura em ºF
print(f'A temperatura de {celsius}ºC é igual a {fahrenheit}ºF.')
