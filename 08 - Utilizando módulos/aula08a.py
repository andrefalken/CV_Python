# Importando toda a biblioteca de matemática
import math

# Declarando a variável para receber um número inteiro
numero = int(input('Digite um número inteiro: '))

# Declarando a variável para receber a raiz quadrada da biblioteca math que é math.sqrt()
raiz_quadrada = math.sqrt(numero)

# Imprimindo a raiz quadrada da variável numero math.sqrt(numero)
print(f'A raiz quadrada de {numero} é igual a {raiz_quadrada:.2f}.')
